package object;

public class Starring {
	String starringname;
	String sex;

	public String getStarringname() {
		return starringname;
	}

	public void setStarringname(String starringname) {
		this.starringname = starringname;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

}
